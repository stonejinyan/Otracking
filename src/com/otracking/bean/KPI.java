package com.otracking.bean;

public class KPI {

	private int target;
	private int wip;
	private int finishGoods;
	private int abnormal;

	public int getTarget() {
		return target;
	}

	public void setTarget(int target) {
		this.target = target;
	}

	public int getWip() {
		return wip;
	}

	public void setWip(int wip) {
		this.wip = wip;
	}

	public int getFinishGoods() {
		return finishGoods;
	}

	public void setFinishGoods(int finishGoods) {
		this.finishGoods = finishGoods;
	}

	public int getAbnormal() {
		return abnormal;
	}

	public void setAbnormal(int abnormal) {
		this.abnormal = abnormal;
	}

}
