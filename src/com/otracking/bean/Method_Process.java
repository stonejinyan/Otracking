package com.otracking.bean;

public class Method_Process {

	private int id;
	private String name;
	private int processline_id;
	private int method_process_type_id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getProcessline_id() {
		return processline_id;
	}

	public void setProcessline_id(int processline_id) {
		this.processline_id = processline_id;
	}

	public int getMethod_process_type_id() {
		return method_process_type_id;
	}

	public void setMethod_process_type_id(int method_process_type_id) {
		this.method_process_type_id = method_process_type_id;
	}

}
