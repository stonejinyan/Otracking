package com.otracking.bean;

public class SEEEOutput {

	private int id;
	private int output_num;
	private String timetag;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getOutput_num() {
		return output_num;
	}

	public void setOutput_num(int output_num) {
		this.output_num = output_num;
	}

	public String getTimetag() {
		return timetag;
	}

	public void setTimetag(String timetag) {
		this.timetag = timetag;
	}

}
